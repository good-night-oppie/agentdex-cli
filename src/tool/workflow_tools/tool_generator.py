    async def _generate_test_cases(self, spec: ToolSpecification) -> List[ToolTestCase]:
        """Use LLM to generate test cases for the tool."""

        system_prompt = dedent("""You are an expert at writing test cases for AI agent tools.
        Generate simple, deterministic test cases that verify basic functionality.
        Each test case should have realistic input arguments that the tool can process
        without requiring external services or complex setup.""")

        user_prompt = dedent(f"""Generate 2-3 simple test cases for the following tool:

        Tool Name: {spec.tool_name}
        Description: {spec.description}
        Parameters: {json.dumps(spec.parameters, indent=2)}
        Implementation Plan: {spec.implementation_plan}

        Each test case needs:
        - name: short descriptive name
        - input: dictionary of arguments matching the tool's parameter schema
        - expected_behavior: what a successful execution should look like

        Keep inputs simple and self-contained (no external file paths, URLs, etc.).
        """)

        messages = [
            SystemMessage(content=system_prompt),
            HumanMessage(content=user_prompt),
        ]

        try:
            response = await model_manager(
                model=self.model_name,
                messages=messages,
                response_format=ToolTestSuite,
            )

            if response.extra and hasattr(response.extra, "parsed_model") and response.extra.parsed_model:
                suite: ToolTestSuite = response.extra.parsed_model
                return suite.test_cases

            suite_dict = json.loads(response.message.strip())
            return [ToolTestCase(**tc) for tc in suite_dict.get("test_cases", suite_dict)]
        except Exception as e:
            logger.warning(f"Failed to generate test cases: {e}")
            return []

    async def _test_tool(self, tool_name: str, spec: ToolSpecification) -> tuple:
        """Generate test cases, run them via TCP, return (passed: bool, details: str)."""

        test_cases = await self._generate_test_cases(spec)
        if not test_cases:
            return True, "No test cases generated; skipping runtime tests."

        results: List[str] = []
        all_passed = True

        for tc in test_cases:
            try:
                logger.info(f"| 🧪 Running test '{tc.name}' with input: {tc.input}")
                response = await tool_manager(name=tool_name, input=tc.input, timeout=60.0)
                if response.success:
                    results.append(f"PASS: {tc.name}")
                    logger.info(f"| ✅ Test '{tc.name}' passed")
                else:
                    all_passed = False
                    results.append(f"FAIL: {tc.name} — {response.message}")
                    logger.warning(f"| ❌ Test '{tc.name}' failed: {response.message}")
            except Exception as e:
                all_passed = False
                results.append(f"ERROR: {tc.name} — {str(e)}")
                logger.error(f"| ❌ Test '{tc.name}' raised exception: {e}")

        details = "; ".join(results)
        return all_passed, details

